var contract;
window.addEventListener("load", async () => {
  if (typeof window.ethereum !== 'undefined') {
    window.web3 = new Web3(window.ethereum);

    try {
      await window.ethereum.enable();
    } catch (error) {
      console.error("User denied account access");
    }
  } else {
    console.error("Web3 provider not found. Consider installing MetaMask.");
  }
  contract = new window.web3.eth.Contract(Content_LIST_ABI, Content_LIST_ADDRESS);
  console.log(contract)
  const accounts = await window.ethereum.request({ method: 'eth_accounts' });
  const balance = await contract.methods.balanceOf(accounts[0]).call()
  // alert(accounts[0])
  const value = await contract.methods.priceInTokens().call()
  document.getElementById("MyTokenWallet").innerHTML = accounts[0]
  document.getElementById("MyTokenBalance").innerHTML = balance
  document.getElementById("MyTokenTransferAmount").value = value
  

});


document.getElementById("transferButton").addEventListener("click",async(e)=>{
  e.preventDefault()
  var address = document.getElementById("MyTokenTransferAddress").value
  var amount = document.getElementById("MyTokenTransferAmount").value
  if(address != "" && amount != ""){
    try{
      const receipt = await contract.methods.confirmTrade().send({from : address })
      console.log(receipt)
      alert("Uploaded successfully")
      window.location.reload(true)
    }catch (error){
      alert("you have already bought the item")
    }
  }
})
